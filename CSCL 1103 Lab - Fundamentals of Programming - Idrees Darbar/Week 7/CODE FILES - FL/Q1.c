// Write a C program that uses a for loop to print all even numbers from 2 to 20.

#include<stdio.h>

int main (void){

    int loop_counter = 1;
    

    for (loop_counter = 1; loop_counter <= 10; loop_counter++){
        printf("The number is: %d\n", loop_counter);

    }

    return 0;
}

